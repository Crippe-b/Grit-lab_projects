export const levelCreator = (l) => {
    let levelString = "";
    for(let row = 0; row < level[l].length; row++) {
        for(let i = 0; i < level[l][row].length; i++){
            switch(level[l][row][i]){
                case 'B':
                    levelString += '<div class="brick" id="normal-brick"></div>';
                    break;
                case 'D':
                    levelString += '<div class="brick" id="double-brick"></div>';
                    break;
                case 'S':
                    levelString += '<div class="brick" id="special-brick"></div>';
                    break;
                default:
                    levelString += '<div class="brick" id="empty-brick"></div>';
                    break;
            }
        }
    }
    document.querySelector(".bricks").innerHTML = levelString;
}